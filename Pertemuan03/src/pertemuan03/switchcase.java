public class switchcase {
    public static void main(String[] args){
        String nVal = "A";
        
        switch (nVal) {
            case "A":
                System.out.print("Bil1 berisikan nilai A");
                break;
            case "B":
                System.out.print("Bil1 berisikan nilai B");
                break;
            case "C":
                System.out.print("Bil1 berisikan nilai C");
                break;
            case "D":
                System.out.print("Bil1 berisikan nilai D");
                break;
            default:
                 System.out.print("Nilai nVal tidak ada dalam definisi");}
              
    }
 
    
}
